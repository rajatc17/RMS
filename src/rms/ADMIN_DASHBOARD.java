    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if(cid.getText().equals(""))
        JOptionPane.showMessageDialog(this,"ENTER USERNAME TO SEARCH RECORDS");
        else
        {
            try
            {
                Connection cn=Connectivity.getConnection();
                PreparedStatement ps=cn.prepareStatement("select * from login where LUser='"+cid.getText()+"'");
                ResultSet rs=ps.executeQuery();
                DefaultTableModel tm=(DefaultTableModel)jTable1.getModel();
                tm.setRowCount(0);
                if (rs.next() == false)
                {
                    JOptionPane.showMessageDialog(this,"NO RECORDS FOUND");
                    tm.addRow(new Object[4]);
                    return;
                }
                rs.beforeFirst();
                while(rs.next())
                {
                    Object o[]={rs.getString("LUser"),rs.getString("LName"),rs.getString("LPass"),rs.getString("LType")};
                    tm.addRow(o);
                }
            }
            catch(Exception e)
            {
                JOptionPane.showMessageDialog(this,e);
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

