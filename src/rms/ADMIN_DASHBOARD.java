    private void update(JTable ob)
    {
        ob.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        ob.getTableHeader().setFont(new Font("Bahnschrift", Font.BOLD, 14));
        try
        { 
            Connection cn=Connectivity.getConnection();
            PreparedStatement ps=cn.prepareStatement("select * from login");
            ResultSet rs=ps.executeQuery();
            DefaultTableModel tm=(DefaultTableModel)ob.getModel();
            tm.setRowCount(0);
            while(rs.next())
            {   
                Object o[]={rs.getString("LUser"),rs.getString("LName"),rs.getString("LPass"),rs.getString("LType")};
                tm.addRow(o);
            }
        }
    catch(Exception e)
    {   
        JOptionPane.showMessageDialog(this,e);
    }
    }
